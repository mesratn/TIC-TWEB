                                <p>Breakfast is a big deal around here.  it really always has been, but it has been THE meal since i've been pregnant.  it doesn't matter what time of day it is, or what meal i should be eating, breakfast items are always on my mind, and somehow always make it onto my plate.  some of the many forms our dinners consist of is: spicy egg burritos with avocado and tons of hot sauce, frittatas with greens and asparagus, or just a plain old granola situation with some fruit toppings. basically what i'm saying is that i find a way to get a serving of morning-type meal whatever the situation or time of day.</p>

                                <p>Chia pudding, on the other hand, was something i never thought of as a breakfast option, which is really silly because chia seeds are the mightiest, nourishing little things that ever existed!  i've always saved chia pudding for dessert or an afternoon snack.  but as i was floating through the endless abyss that is pinterest, i came across some really beautiful and inspiring recipes for chia parfaits that i knew i wanted to try at some point.  i started with making a vanilla-based pudding because even though i prefer chocolate, it seemed like a more appropriate morning meal.  each time i made it i felt like the chia flavor came through too much? maybe i need a different recipe?  but i moved on to making a chocolate version with layers of soaked, overnight oats and topped with fresh fruit. it was super delicious, but at the same time i felt like the textures of the oats and chia were fighting each other, so in the end i went with a nutty, crunchy, salty-sweet granola and a bit of tangy coconut yogurt...</p>

